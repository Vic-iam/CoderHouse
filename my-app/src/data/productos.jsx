let error = false;

export const getProducts = () => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      if (error) {
        reject("Hubo un error");
      } else {
        resolve(mangas);
      }
    }, 3000);
  });
};

export const getOneProduct = (id) => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      const mang = mangas.find((man) => man.id === Number(id));

      if (mang) {
        resolve(mang);
      } else {
        reject("El producto no existe");
      }
    }, 1);
  });
};
